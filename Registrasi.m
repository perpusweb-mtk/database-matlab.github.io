% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
filename = 'Data_Mahasiswa.xlsx';
no = xlsread(filename,'B:B');
 
nim = get(handles.edit1,'String');
nama = get(handles.edit2,'String');
laki2 = get(handles.radiobutton1,'Value');
perempuan = get(handles.radiobutton2,'Value');
if laki2 == 1
    jenis_kel = 'Laki-laki';
elseif perempuan == 1
    jenis_kel = 'Perempuan';
end
ttl = get(handles.edit3,'String');
alamat = get(handles.edit4,'String');
tahun_masuk = get(handles.edit5,'String');
minat = get(handles.popupmenu1,'Value');
if minat == 1
    bidang_min = 'Statistika';
elseif minat == 2
    bidang_min = 'Aljabar';
elseif minat == 3
    bidang_min = 'Matematika Terapan';
elseif minat == 4
    bidang_min = 'Komputasi';
elseif minat == 5
    bidang_min = 'Analisis';
end
 
if numel(no) == 0
    no = 1;
    range = 'B6';
else
    no = numel(no)+1;
    range = strcat(['B',num2str(no+4)]);
end
 
if isempty(nim)
    warndlg('NIM tidak boleh kosong','Warning');
elseif isempty(nama)
    warndlg('Nama tidak boleh kosong','Warning');
else
    data = table(cell({no,nim,nama,jenis_kel,ttl,alamat,tahun_masuk,bidang_min}));
    writetable(data,filename,'Sheet',1,'Range',range,'WriteVariableNames',false)
    msgbox(strcat(['Data Mahasiswa No. ',num2str(no),' berhasil disimpan']),'Message')
end

